md" ## Acid and Base Theories
Originally, the arrhenius definition was first used to understand acid and bases. It said that acids donate H+ ions and bases donate OH- ions. However, people figured out that this did not apply for all bases, like NH3+. So, the definition was revised to with the Bronsted-Lowry definition which says:

Acids donate protons (h+ ions)

Bases accept protons (h+ ions)

**Conjugate acid-base pair**: this a pair that differ with the prescence of an h+ ion. Some examples of these pairs are: $\ce{NH3 and NH4+, H2PO4- and H3PO4}$. The conjugate pair is the resulting species after the h+ is either donated or accepted. The formation of different species dictates whether the solution is acidic or basic. If H3O+ is formed, then the solution is acidic. If OH- is formed, then the solution is neutral. If equal amounts of H+ and OH- are formed, then the solution is neutral.

*Acids only donate one proton at a time*
